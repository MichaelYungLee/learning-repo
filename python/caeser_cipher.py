def caeser(text, shift, direction):
    cipher_text = ""
    if shift > 26:
        shift = shift % 26
    shift = shift if direction == "encode" else 26 - shift
    for letter in text:
        if letter.isalpha():
            shifted_index = (alphabet.index(letter) + shift) % 26
            cipher_text += alphabet[shifted_index]
        else:
            cipher_text += letter
    return cipher_text

